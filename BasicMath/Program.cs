namespace Lab7;

public class BasicMath
{
    public BasicMath()
    {

    }
    static public double Add(double number1, double number2)
    {
        return number1 + number2;
    }

    static public double Subtract(double number1, double number2)
    {
        return number1 - number2;
    }

    static public double Divide(double number1, double number2)
    {
        return number1 / number2;
    }

    static public double Multiply(double number1, double number2)
    {
        return number1 * number2;
    }
}



